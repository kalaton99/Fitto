export const getDoodleSize = (size: 'sm' | 'md' | 'lg' | 'xl' = 'md'): string => {
  const sizes = {
    sm: 'w-8 h-8',
    md: 'w-12 h-12',
    lg: 'w-16 h-16',
    xl: 'w-24 h-24',
  };
  return sizes[size];
};

